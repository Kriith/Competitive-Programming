const int mod = 1e9+7;

ull binpow(ull a, ull b) {
    a %= mod;
    ull res = 1;
    while (b > 0) {
        if (b & 1)
            res = res * a % mod;
        a = a * a % mod;
        b >>= 1;
    }
    return res;
}

